
#extend
fruits = ['apple', 'banana', 'cherry']

cars = ['Ford', 'BMW', 'Volvo']

fruits.extend(cars)

#pop
fruits = ['apple', 'banana', 'cherry']

fruits.pop(1)

print(fruits)

# Remove the "banana" element of the fruit list:

fruits = ['apple', 'banana', 'cherry']

fruits.remove("banana")

#reverse
fruits = ['apple', 'banana', 'cherry']

fruits.reverse()